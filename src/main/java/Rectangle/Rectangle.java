package Rectangle;

public class Rectangle {
    private final int length, breadth;
    public Rectangle(int length, int breadth){
        this.length = length;
        this.breadth = breadth;
    }

    public int getLength() {
        return length;
    }

    public int getBreadth() {
        return breadth;
    }

    public int area() {
        return 20;
    }
}
